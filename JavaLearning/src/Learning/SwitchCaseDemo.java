package Learning;

public class SwitchCaseDemo {

	
	public static void main(String[] args) {
		
		int monthNumber=3;
		
		switch(monthNumber) {
		
		case 1 : 
			
		System.out.println("JAN");
		break;
		
		case 2 : 
			
		System.out.println("Feb");
		break;
			
		case 3 : 
				
		System.out.println("Mar");
		break;	
		
		case 4 :
			
		System.out.println("Mar");
		break;	
		
		default :
			
		System.out.println("Number is invalid");	
			
		}
	}
}
